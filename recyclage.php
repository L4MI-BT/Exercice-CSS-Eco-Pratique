            <p>
                Le recyclage est un procédé de traitement des déchets (industriels ou ménagers) de produits arrivés en fin de vie, qui permet de réintroduire certains de leurs matériaux dans la production de nouveaux produits. Les matériaux recyclables comprennent des métaux, plastiques et cartons, le verre, les gravats, etc.
                Un exemple de ce procédé est la fabrication de bouteilles neuves avec le verre de bouteilles usagées, même si elle est considérablement moins efficace énergétiquement que le système des consignes.
            </p>
            <p>
                Le recyclage a deux conséquences environnementales majeures :
                <ul>
                    <li>la réduction du volume de déchets, donc de la pollution qu'ils causeraient (certains matériaux mettent des décennies, voire des siècles, à se dégrader)</li>
                    <li>la préservation des ressources naturelles, puisque la matière recyclée est utilisée à la place de celle qu'on aurait dû extraire.</li>
                </ul>
            </p>
            <p>
                l représente une des activités économiques de la société de consommation. Certains procédés sont simples et bon marché, tandis que d'autres sont complexes et peu rentables. Dans ce domaine, les objectifs de l'écologie et ceux du commerce peuvent ainsi se rejoindre ou diverger ; la législation peut alors imposer la prise en charge de cette externalité. Ainsi, en particulier depuis les années 1970, le recyclage est une activité importante de l'économie et des conditions de vie des pays développés.
            </p>
